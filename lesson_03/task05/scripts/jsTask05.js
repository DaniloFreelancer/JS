let category = prompt(
  "Введіть назву категорії водія (А-мотоцикл, В-легковий автомобіль, С-вантажний автомобіль):"
);

category = category.toUpperCase();

if (category === `A`){
	alert("Ви можете керувати мотоциклом.");
}
else if (category === `B`)
	alert("Ви можете керувати легковим автомобілем.");
else if (category === `C`)
	alert("Ви можете керувати вантажним автомобілем.");
else 
	alert("Невідома категорія. Введіть A, B або C.");